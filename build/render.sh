#!/usr/bin/env bash
# rolepod render — assembles per-CLI entry doc from core/ fragments + adapter template.
#
# Usage:
#   ./build/render.sh --target=claude            # default
#   ./build/render.sh --target=codex
#   ./build/render.sh --target=gemini
#   ./build/render.sh --target=all               # render all three
#
# Outputs:
#   build/rendered/claude/CLAUDE.md
#   build/rendered/claude/agents/<name>.md             # 18 files (Claude frontmatter)
#   build/rendered/codex/AGENTS.md
#   build/rendered/codex/agents/<name>.md              # 18 files (portable frontmatter)
#   build/rendered/gemini/GEMINI.md
#
# Template directives (one per line):
#   {{INCLUDE: <path-relative-to-repo-root>}}
# The directive line is replaced verbatim with the contents of <path>.
# Includes are NOT recursive — fragments cannot include other fragments.

set -euo pipefail

REPO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
TARGET="claude"

for arg in "$@"; do
  case "$arg" in
    --target=*) TARGET="${arg#--target=}" ;;
    -h|--help)
      sed -n '2,21p' "$0"
      exit 0 ;;
    *) echo "Unknown arg: $arg" >&2; exit 1 ;;
  esac
done

case "$TARGET" in
  claude|codex|gemini|all) ;;
  *)
    echo "Unknown target: $TARGET (expected claude|codex|gemini|all)" >&2
    exit 1 ;;
esac

# ─── Generate skill-index fragment from SKILL.md frontmatter ───────────────
# Each SKILL.md has YAML frontmatter with `name:` and `description:` keys.
# Fragment format: one row per skill, sorted by name, plus a header.

generate_skill_index() {
  local out="$REPO_DIR/core/fragments/skill-index.md"
  local skills_dir="$REPO_DIR/core/skills"

  # Phase membership for the 6-phase lifecycle taxonomy. A skill listed in
  # multiple phases appears in the first matching phase.
  # Cross-cutting skills (zoom-out, source-driven-development, context-engineering)
  # are bucketed last.
  emit_phase_section() {
    local phase_title="$1"; shift
    local -a phase_skills=("$@")
    echo ""
    echo "### $phase_title"
    echo ""
    echo "| Skill | Description | Path |"
    echo "|-------|-------------|------|"
    local skill_name
    for skill_name in "${phase_skills[@]}"; do
      local skill_md="$skills_dir/$skill_name/SKILL.md"
      [ -f "$skill_md" ] || continue
      local rel_path="${skill_md#$REPO_DIR/}"
      local name desc
      name=$(awk '/^name:/{sub(/^name:[[:space:]]*/, ""); print; exit}' "$skill_md" | sed -e 's/^["'\'']//' -e 's/["'\'']$//')
      desc=$(awk '/^description:/{sub(/^description:[[:space:]]*/, ""); print; exit}' "$skill_md" | sed -e 's/^["'\'']//' -e 's/["'\'']$//')
      [ -z "$name" ] && name="$skill_name"
      [ -z "$desc" ] && desc=""
      # Truncate description to 100 unicode characters. Use python so the
      # cut point is locale-independent — bash `${#var}` counts BYTES on
      # macOS (no LANG) but CHARS on Linux with UTF-8 locale, which would
      # produce different output across systems for descriptions
      # containing em-dash / arrows / other multi-byte chars and break
      # the render-clean gate.
      desc=$(printf '%s' "$desc" | python3 -c 'import sys; s=sys.stdin.read(); print(s if len(s) <= 100 else s[:97] + "...", end="")')
      desc="${desc//|/\\|}"
      echo "| \`$name\` | $desc | \`$rel_path\` |"
    done
  }

  {
    echo "<!-- Auto-generated by build/render.sh from core/skills/*/SKILL.md frontmatter. Do not edit. -->"
    echo "<!-- Tiered: Tier 0 router → Tier 1 core workflow → Tier 2 specialist (grouped by phase) → Tier 3 compat shims. -->"

    # Tier 0 — Router. Loaded first on every request.
    emit_phase_section "Tier 0 — Router" \
      using-rolepod

    # Tier 1 — Core workflow. The default spine Lead runs through.
    # Define → Plan → Build → Verify → Review → Ship.
    emit_phase_section "Tier 1 — Core Workflow" \
      spec-driven-development \
      planning-and-task-breakdown \
      systematic-debugging \
      test-driven-development \
      team-routing \
      parallel-contract-orchestration \
      subagent-task-execution \
      post-change-verify \
      code-review-and-quality \
      pre-merge-gate \
      code-simplification

    # Tier 2 — Specialist. Fire by domain match inside Build/Verify/Review/Ship.
    emit_phase_section "Tier 2 — Specialist · Plan" \
      api-and-interface-design

    emit_phase_section "Tier 2 — Specialist · Build" \
      anti-spaghetti \
      claude-api \
      conversion-copywriting \
      doc-coauthoring \
      frontend-ui-engineering \
      interaction-design \
      interface-design \
      using-worktrees

    emit_phase_section "Tier 2 — Specialist · Verify" \
      browser-testing-with-devtools \
      performance-optimization \
      security-and-hardening \
      webapp-testing

    emit_phase_section "Tier 2 — Specialist · Review" \
      doubt-driven-development \
      web-design-guidelines

    emit_phase_section "Tier 2 — Specialist · Ship" \
      ci-cd-and-automation \
      documentation-and-adrs \
      finishing-a-development-branch \
      internal-comms \
      seo \
      shipping-and-launch \
      user-facing-content

    emit_phase_section "Tier 2 — Specialist · Cross-cutting" \
      context-engineering \
      source-driven-development \
      zoom-out

    # Tier 3 — Compatibility shims. Old skill paths still routed via legacy
    # trigger phrases; bodies redirect to the canonical Tier 1 skill.
    emit_phase_section "Tier 3 — Compatibility shims" \
      debugging-and-error-recovery \
      root-cause-tracing
  } > "$out"
}

# ─── Generate agent-roster fragment from core/agents/*.md frontmatter ──────
# Used by Codex/Gemini templates (Lead-only mode roster — no auto-spawn).

generate_agent_roster() {
  local out="$REPO_DIR/core/fragments/agent-roster.md"
  local agents_dir="$REPO_DIR/core/agents"

  {
    echo "<!-- Auto-generated by build/render.sh from core/agents/*.md frontmatter. Do not edit. -->"
    echo ""
    echo "| Agent | Description |"
    echo "|-------|-------------|"
    for agent_md in "$agents_dir"/*.md; do
      [ -f "$agent_md" ] || continue
      local name desc
      name=$(awk '/^name:/{sub(/^name:[[:space:]]*/, ""); print; exit}' "$agent_md")
      desc=$(awk '/^description:/{sub(/^description:[[:space:]]*/, ""); print; exit}' "$agent_md")
      desc="${desc//|/\\|}"
      echo "| \`$name\` | $desc |"
    done | sort -t'|' -k2,2
  } > "$out"
}

# ─── Render template, resolving {{INCLUDE: ...}} directives ─────────────────
# Reads template line-by-line; lines matching `{{INCLUDE: <path>}}` are
# replaced with file contents. All other lines pass through verbatim.

render_template() {
  local template="$1"
  local output="$2"
  local line path inc_file

  : > "$output"
  while IFS= read -r line || [ -n "$line" ]; do
    case "$line" in
      "{{INCLUDE: "*"}}")
        path="${line#'{{INCLUDE: '}"
        path="${path%'}}'}"
        inc_file="$REPO_DIR/$path"
        if [ ! -f "$inc_file" ]; then
          echo "render: missing include $path (referenced from $template)" >&2
          exit 1
        fi
        cat "$inc_file" >> "$output"
        ;;
      *)
        printf '%s\n' "$line" >> "$output"
        ;;
    esac
  done < "$template"
}

# ─── Render per-target agent files (Claude / Codex frontmatter shape) ──────

render_agents() {
  local target="$1"
  local out_dir="$REPO_DIR/build/rendered/$target/agents"
  mkdir -p "$out_dir"
  local count=0
  for core_md in "$REPO_DIR"/core/agents/*.md; do
    local name
    name="$(basename "$core_md" .md)"
    python3 "$REPO_DIR/build/merge-agent.py" --target="$target" --name="$name" \
      > "$out_dir/$name.md"
    count=$((count + 1))
  done
  echo "rendered $count agents → build/rendered/$target/agents/"
}

# ─── Render Claude target ───────────────────────────────────────────────────

render_claude() {
  local template="$REPO_DIR/adapters/claude/CLAUDE.md.tmpl"
  local out_dir="$REPO_DIR/build/rendered/claude"
  local output="$out_dir/CLAUDE.md"

  [ -f "$template" ] || { echo "render: missing $template" >&2; exit 1; }

  mkdir -p "$out_dir"
  render_template "$template" "$output"
  render_agents "claude"
}

# ─── Render Codex target ────────────────────────────────────────────────────
# Phase 2.4: Codex ships as a Codex marketplace consumable so the native
# plugin loader actually wires up agents/skills/hooks (not just AGENTS.md text).
# Output layout matches the bundled-marketplace shape Codex 0.130+ scans:
#   AGENTS.md                                        (entry doc, ~/.codex/AGENTS.md)
#   .agents/plugins/marketplace.json                 (marketplace manifest)
#   plugins/rolepod/.codex-plugin/plugin.json        (plugin manifest)
#   plugins/rolepod/agents/*.toml                    (18 portable agents)
#   plugins/rolepod/hooks/hooks.json + *.sh          (4 hook scripts)
#   plugins/rolepod/skills/<name>/SKILL.md ...       (real dir, copied from core/skills)

render_codex() {
  local template="$REPO_DIR/adapters/codex/AGENTS.md.tmpl"
  local out_dir="$REPO_DIR/build/rendered/codex"
  local output="$out_dir/AGENTS.md"
  local adapter_dir="$REPO_DIR/adapters/codex"
  local plugin_src="$adapter_dir/plugins/rolepod"
  local plugin_dst="$out_dir/plugins/rolepod"

  [ -f "$template" ] || { echo "render: missing $template" >&2; exit 1; }

  # Clean stale artifacts from previous runs (e.g. old top-level .codex-plugin/).
  rm -rf "$out_dir"
  mkdir -p "$out_dir"
  render_template "$template" "$output"

  # Marketplace manifest at .agents/plugins/marketplace.json.
  if [ -f "$adapter_dir/.agents/plugins/marketplace.json" ]; then
    mkdir -p "$out_dir/.agents/plugins"
    cp "$adapter_dir/.agents/plugins/marketplace.json" "$out_dir/.agents/plugins/"
  else
    echo "render: missing $adapter_dir/.agents/plugins/marketplace.json" >&2; exit 1
  fi

  # Plugin manifest under plugins/rolepod/.codex-plugin/.
  if [ -d "$plugin_src/.codex-plugin" ]; then
    mkdir -p "$plugin_dst"
    cp -R "$plugin_src/.codex-plugin" "$plugin_dst/"
  else
    echo "render: missing $plugin_src/.codex-plugin/" >&2; exit 1
  fi

  # TOML agents.
  if [ -d "$plugin_src/agents" ]; then
    mkdir -p "$plugin_dst/agents"
    cp "$plugin_src/agents"/*.toml "$plugin_dst/agents/" 2>/dev/null || true
  fi

  # Hooks (json + executable scripts).
  if [ -d "$plugin_src/hooks" ]; then
    cp -R "$plugin_src/hooks" "$plugin_dst/"
    chmod +x "$plugin_dst/hooks/"*.sh 2>/dev/null || true
  fi

  # Dereference skills symlink into a real directory tree (tarball / commit safe).
  mkdir -p "$plugin_dst/skills"
  for skill_dir in "$REPO_DIR"/core/skills/*/; do
    local name; name="$(basename "$skill_dir")"
    cp -R "$skill_dir" "$plugin_dst/skills/$name"
  done
}

# ─── Render Gemini target ───────────────────────────────────────────────────
# Phase 2.3: Gemini now ships as a native extension. Output layout:
#   GEMINI.md                       (entry doc, written to ~/.gemini/GEMINI.md)
#   gemini-extension.json           (extension manifest)
#   commands/*.toml                 (6 slash commands)
#   hooks/hooks.json + *.sh         (3 hook scripts)
#   skills/<name>/SKILL.md ...      (real dir, copied from core/skills/)

render_gemini() {
  local template="$REPO_DIR/adapters/gemini/GEMINI.md.tmpl"
  local out_dir="$REPO_DIR/build/rendered/gemini"
  local output="$out_dir/GEMINI.md"
  local adapter_dir="$REPO_DIR/adapters/gemini"

  [ -f "$template" ] || { echo "render: missing $template" >&2; exit 1; }

  rm -rf "$out_dir"
  mkdir -p "$out_dir"
  render_template "$template" "$output"

  # Extension manifest.
  if [ -f "$adapter_dir/gemini-extension.json" ]; then
    cp "$adapter_dir/gemini-extension.json" "$out_dir/"
  else
    echo "render: missing $adapter_dir/gemini-extension.json" >&2; exit 1
  fi

  # Slash commands.
  if [ -d "$adapter_dir/commands" ]; then
    mkdir -p "$out_dir/commands"
    cp "$adapter_dir/commands"/*.toml "$out_dir/commands/" 2>/dev/null || true
  fi

  # Hooks.
  if [ -d "$adapter_dir/hooks" ]; then
    cp -R "$adapter_dir/hooks" "$out_dir/"
    chmod +x "$out_dir/hooks/"*.sh 2>/dev/null || true
  fi

  # Skills as real directory tree (Gemini extension expects directories,
  # not symlinks — render-time copy guarantees this regardless of how the
  # adapter side stores them).
  mkdir -p "$out_dir/skills"
  for skill_dir in "$REPO_DIR"/core/skills/*/; do
    local name; name="$(basename "$skill_dir")"
    cp -R "$skill_dir" "$out_dir/skills/$name"
  done
}

generate_skill_index
generate_agent_roster

case "$TARGET" in
  claude) render_claude ;;
  codex)  render_codex ;;
  gemini) render_gemini ;;
  all)    render_claude; render_codex; render_gemini ;;
esac
