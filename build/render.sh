#!/usr/bin/env bash
# rolepod render — assembles per-CLI entry doc from core/ fragments + adapter template.
#
# Usage:
#   ./build/render.sh --target=claude            # default
#   ./build/render.sh --target=codex             # Phase 2.2 — not yet implemented
#   ./build/render.sh --target=gemini            # Phase 2.2 — not yet implemented
#
# Outputs:
#   build/rendered/<target>/<entry-doc>
#
# Template directives (one per line):
#   {{INCLUDE: <path-relative-to-repo-root>}}
# The directive line is replaced verbatim with the contents of <path>.
# Includes are NOT recursive — fragments cannot include other fragments.

set -euo pipefail

REPO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
TARGET="claude"

for arg in "$@"; do
  case "$arg" in
    --target=*) TARGET="${arg#--target=}" ;;
    -h|--help)
      sed -n '2,16p' "$0"
      exit 0 ;;
    *) echo "Unknown arg: $arg" >&2; exit 1 ;;
  esac
done

case "$TARGET" in
  claude) ;;
  codex|gemini)
    echo "Target '$TARGET' is Phase 2.2 and not yet implemented." >&2
    echo "Run with --target=claude instead." >&2
    exit 1 ;;
  *)
    echo "Unknown target: $TARGET (expected claude|codex|gemini)" >&2
    exit 1 ;;
esac

# ─── Generate skill-index fragment from SKILL.md frontmatter ───────────────
# Each SKILL.md has YAML frontmatter with `name:` and `description:` keys.
# Fragment format: one row per skill, sorted by name, plus a header.

generate_skill_index() {
  local out="$REPO_DIR/core/fragments/skill-index.md"
  local skills_dir="$REPO_DIR/core/skills"

  {
    echo "<!-- Auto-generated by build/render.sh from core/skills/*/SKILL.md frontmatter. Do not edit. -->"
    echo ""
    echo "| Skill | Description | Path |"
    echo "|-------|-------------|------|"
    for skill_md in "$skills_dir"/*/SKILL.md; do
      [ -f "$skill_md" ] || continue
      local rel_path="${skill_md#$REPO_DIR/}"
      local name desc
      # Extract first line matching `name:` and `description:` from YAML frontmatter (first 20 lines).
      name=$(awk '/^name:/{sub(/^name:[[:space:]]*/, ""); print; exit}' "$skill_md" | sed -e 's/^["'\'']//' -e 's/["'\'']$//')
      desc=$(awk '/^description:/{sub(/^description:[[:space:]]*/, ""); print; exit}' "$skill_md" | sed -e 's/^["'\'']//' -e 's/["'\'']$//')
      [ -z "$name" ] && name="$(basename "$(dirname "$skill_md")")"
      [ -z "$desc" ] && desc=""
      # Truncate description to keep table readable (first 100 chars).
      if [ "${#desc}" -gt 100 ]; then
        desc="${desc:0:97}..."
      fi
      # Escape pipe chars so they don't break the markdown table.
      desc="${desc//|/\\|}"
      echo "| \`$name\` | $desc | \`$rel_path\` |"
    done | sort -t'|' -k2,2
  } > "$out"
}

# ─── Render template, resolving {{INCLUDE: ...}} directives ─────────────────
# Reads template line-by-line; lines matching `{{INCLUDE: <path>}}` are
# replaced with file contents. All other lines pass through verbatim.

render_template() {
  local template="$1"
  local output="$2"
  local line path inc_file

  : > "$output"
  while IFS= read -r line || [ -n "$line" ]; do
    case "$line" in
      "{{INCLUDE: "*"}}")
        path="${line#'{{INCLUDE: '}"
        path="${path%'}}'}"
        inc_file="$REPO_DIR/$path"
        if [ ! -f "$inc_file" ]; then
          echo "render: missing include $path (referenced from $template)" >&2
          exit 1
        fi
        cat "$inc_file" >> "$output"
        ;;
      *)
        printf '%s\n' "$line" >> "$output"
        ;;
    esac
  done < "$template"
}

# ─── Render Claude target ───────────────────────────────────────────────────

render_claude() {
  local template="$REPO_DIR/adapters/claude/CLAUDE.md.tmpl"
  local out_dir="$REPO_DIR/build/rendered/claude"
  local output="$out_dir/CLAUDE.md"

  [ -f "$template" ] || { echo "render: missing $template" >&2; exit 1; }

  mkdir -p "$out_dir"
  render_template "$template" "$output"
}

generate_skill_index
case "$TARGET" in
  claude) render_claude ;;
esac
