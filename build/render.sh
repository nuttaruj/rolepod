#!/usr/bin/env bash
# rolepod render — assembles per-CLI entry doc from core/ fragments + adapter template.
#
# Usage:
#   ./build/render.sh --target=claude            # default
#   ./build/render.sh --target=codex
#   ./build/render.sh --target=gemini
#   ./build/render.sh --target=all               # render all three
#
# Outputs:
#   build/rendered/claude/CLAUDE.md
#   build/rendered/claude/agents/<name>.md             # 18 files (Claude frontmatter)
#   build/rendered/codex/AGENTS.md
#   build/rendered/codex/agents/<name>.md              # 18 files (portable frontmatter)
#   build/rendered/gemini/GEMINI.md
#
# Template directives (one per line):
#   {{INCLUDE: <path-relative-to-repo-root>}}
# The directive line is replaced verbatim with the contents of <path>.
# Includes are NOT recursive — fragments cannot include other fragments.

set -euo pipefail

REPO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
TARGET="claude"

for arg in "$@"; do
  case "$arg" in
    --target=*) TARGET="${arg#--target=}" ;;
    -h|--help)
      sed -n '2,21p' "$0"
      exit 0 ;;
    *) echo "Unknown arg: $arg" >&2; exit 1 ;;
  esac
done

case "$TARGET" in
  claude|codex|gemini|all) ;;
  *)
    echo "Unknown target: $TARGET (expected claude|codex|gemini|all)" >&2
    exit 1 ;;
esac

# ─── Generate skill-index fragment from SKILL.md frontmatter ───────────────
# Each SKILL.md has YAML frontmatter with `name:` and `description:` keys.
# Fragment format: one row per skill, sorted by name, plus a header.

generate_skill_index() {
  local out="$REPO_DIR/core/fragments/skill-index.md"
  local skills_dir="$REPO_DIR/core/skills"

  {
    echo "<!-- Auto-generated by build/render.sh from core/skills/*/SKILL.md frontmatter. Do not edit. -->"
    echo ""
    echo "| Skill | Description | Path |"
    echo "|-------|-------------|------|"
    for skill_md in "$skills_dir"/*/SKILL.md; do
      [ -f "$skill_md" ] || continue
      local rel_path="${skill_md#$REPO_DIR/}"
      local name desc
      # Extract first line matching `name:` and `description:` from YAML frontmatter (first 20 lines).
      name=$(awk '/^name:/{sub(/^name:[[:space:]]*/, ""); print; exit}' "$skill_md" | sed -e 's/^["'\'']//' -e 's/["'\'']$//')
      desc=$(awk '/^description:/{sub(/^description:[[:space:]]*/, ""); print; exit}' "$skill_md" | sed -e 's/^["'\'']//' -e 's/["'\'']$//')
      [ -z "$name" ] && name="$(basename "$(dirname "$skill_md")")"
      [ -z "$desc" ] && desc=""
      # Truncate description to keep table readable (first 100 chars).
      if [ "${#desc}" -gt 100 ]; then
        desc="${desc:0:97}..."
      fi
      # Escape pipe chars so they don't break the markdown table.
      desc="${desc//|/\\|}"
      echo "| \`$name\` | $desc | \`$rel_path\` |"
    done | sort -t'|' -k2,2
  } > "$out"
}

# ─── Generate agent-roster fragment from core/agents/*.md frontmatter ──────
# Used by Codex/Gemini templates (Lead-only mode roster — no auto-spawn).

generate_agent_roster() {
  local out="$REPO_DIR/core/fragments/agent-roster.md"
  local agents_dir="$REPO_DIR/core/agents"

  {
    echo "<!-- Auto-generated by build/render.sh from core/agents/*.md frontmatter. Do not edit. -->"
    echo ""
    echo "| Agent | Description |"
    echo "|-------|-------------|"
    for agent_md in "$agents_dir"/*.md; do
      [ -f "$agent_md" ] || continue
      local name desc
      name=$(awk '/^name:/{sub(/^name:[[:space:]]*/, ""); print; exit}' "$agent_md")
      desc=$(awk '/^description:/{sub(/^description:[[:space:]]*/, ""); print; exit}' "$agent_md")
      desc="${desc//|/\\|}"
      echo "| \`$name\` | $desc |"
    done | sort -t'|' -k2,2
  } > "$out"
}

# ─── Render template, resolving {{INCLUDE: ...}} directives ─────────────────
# Reads template line-by-line; lines matching `{{INCLUDE: <path>}}` are
# replaced with file contents. All other lines pass through verbatim.

render_template() {
  local template="$1"
  local output="$2"
  local line path inc_file

  : > "$output"
  while IFS= read -r line || [ -n "$line" ]; do
    case "$line" in
      "{{INCLUDE: "*"}}")
        path="${line#'{{INCLUDE: '}"
        path="${path%'}}'}"
        inc_file="$REPO_DIR/$path"
        if [ ! -f "$inc_file" ]; then
          echo "render: missing include $path (referenced from $template)" >&2
          exit 1
        fi
        cat "$inc_file" >> "$output"
        ;;
      *)
        printf '%s\n' "$line" >> "$output"
        ;;
    esac
  done < "$template"
}

# ─── Render per-target agent files (Claude / Codex frontmatter shape) ──────

render_agents() {
  local target="$1"
  local out_dir="$REPO_DIR/build/rendered/$target/agents"
  mkdir -p "$out_dir"
  local count=0
  for core_md in "$REPO_DIR"/core/agents/*.md; do
    local name
    name="$(basename "$core_md" .md)"
    python3 "$REPO_DIR/build/merge-agent.py" --target="$target" --name="$name" \
      > "$out_dir/$name.md"
    count=$((count + 1))
  done
  echo "rendered $count agents → build/rendered/$target/agents/"
}

# ─── Render Claude target ───────────────────────────────────────────────────

render_claude() {
  local template="$REPO_DIR/adapters/claude/CLAUDE.md.tmpl"
  local out_dir="$REPO_DIR/build/rendered/claude"
  local output="$out_dir/CLAUDE.md"

  [ -f "$template" ] || { echo "render: missing $template" >&2; exit 1; }

  mkdir -p "$out_dir"
  render_template "$template" "$output"
  render_agents "claude"
}

# ─── Render Codex target ────────────────────────────────────────────────────

render_codex() {
  local template="$REPO_DIR/adapters/codex/AGENTS.md.tmpl"
  local out_dir="$REPO_DIR/build/rendered/codex"
  local output="$out_dir/AGENTS.md"

  [ -f "$template" ] || { echo "render: missing $template" >&2; exit 1; }

  mkdir -p "$out_dir"
  render_template "$template" "$output"
  render_agents "codex"

  # Copy plugin manifest verbatim (best-effort schema; see adapter README).
  if [ -f "$REPO_DIR/adapters/codex/manifest.json" ]; then
    cp "$REPO_DIR/adapters/codex/manifest.json" "$out_dir/manifest.json"
  fi
}

# ─── Render Gemini target ───────────────────────────────────────────────────

render_gemini() {
  # Gemini target: agent roster inlined in GEMINI.md (no per-agent files —
  # Gemini has no native sub-agent loader). See docs/cli-support.md.
  local template="$REPO_DIR/adapters/gemini/GEMINI.md.tmpl"
  local out_dir="$REPO_DIR/build/rendered/gemini"
  local output="$out_dir/GEMINI.md"

  [ -f "$template" ] || { echo "render: missing $template" >&2; exit 1; }

  mkdir -p "$out_dir"
  render_template "$template" "$output"
}

generate_skill_index
generate_agent_roster

case "$TARGET" in
  claude) render_claude ;;
  codex)  render_codex ;;
  gemini) render_gemini ;;
  all)    render_claude; render_codex; render_gemini ;;
esac
